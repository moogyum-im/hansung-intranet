async function main() {
  console.log('🚀 입찰 데이터 시딩 시작');

  // 기존 삭제
  const { data: existing } = await supabase.from('bid_projects').select('id, title');
  if (existing?.length) {
    console.log('기존 프로젝트 삭제...');
    await supabase.from('bid_projects').delete().in('id', existing.map(p => p.id));
  }

  // 프로젝트 생성
  const { data: proj, error: projErr } = await supabase
    .from('bid_projects')
    .insert({ title: '조경공사 입찰 비교', description: '', bid_date: '2025-05-29' })
    .select().single();
  if (projErr) { console.error('❌ 프로젝트 생성 실패:', projErr.message); return; }
  console.log('✅ 프로젝트 생성:', proj.id);

  // 회사 생성
  await supabase.from('bid_companies').insert([
    { project_id: proj.id, company_key: 'A', company_name: 'D사', is_ours: false },
    { project_id: proj.id, company_key: 'B', company_name: 'H사', is_ours: true },
    { project_id: proj.id, company_key: 'C', company_name: 'J사', is_ours: false },
  ]);

  // 항목 + 비용 삽입
  let ok = 0;
  for (let i = 0; i < ITEMS.length; i++) {
    const [name, spec, unit, qty, dPrice, dAmt, hPrice, hAmt, jPrice, jAmt] = ITEMS[i];

    const { data: item, error: itemErr } = await supabase
      .from('bid_items')
      .insert({ project_id: proj.id, item_name: name, spec, unit, quantity: qty, display_order: i + 1 })
      .select().single();

    if (itemErr) { console.error(`❌ [${name}]:`, itemErr.message); continue; }

    const { error: costErr } = await supabase.from('bid_costs').insert([
      { item_id: item.id, company_key: 'A', unit_price: dPrice, amount: dAmt },
      { item_id: item.id, company_key: 'B', unit_price: hPrice, amount: hAmt },
      { item_id: item.id, company_key: 'C', unit_price: jPrice, amount: jAmt },
    ]);

    if (costErr) { console.error(`❌ 비용 [${name}]:`, costErr.message); continue; }
    ok++;
  }

  console.log(`\n🎉 완료: ${ok}/${ITEMS.length}개 항목 업로드`);
  console.log(`   D사 합계: ${ITEMS.reduce((s,r)=>s+r[5],0).toLocaleString()}원`);
  console.log(`   H사 합계: ${ITEMS.reduce((s,r)=>s+r[7],0).toLocaleString()}원`);
  console.log(`   J사 합계: ${ITEMS.reduce((s,r)=>s+r[9],0).toLocaleString()}원`);
}

main().catch(console.error);
